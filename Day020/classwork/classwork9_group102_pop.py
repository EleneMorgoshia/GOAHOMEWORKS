#pop method - წაშლა ლისთში ინდექსის მეშვეობით
fruits = ["apple",'banana','cherry']
fruits.pop(1) #წაიშლება პირველ ინდექსზე მდგომი ელემენტი
print(fruits)

numbers = [1,2,3,70,80]
numbers.pop(2) #წაიშლება მეორე ინდექსზე მდგომიი, ანუ 3
print(numbers)
numbers.pop(len(numbers)-1) #წაიშლება ბოლო ინდექსზე მდგომი,ანუ 80
print(numbers)